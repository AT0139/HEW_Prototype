/*------------------------------------------------------------------------------
   グローバル変数の定義
------------------------------------------------------------------------------*/
GameFramework g_GameFramework;

//	サウンド用のインデックス
static int g_SoundIndex = 0;
/*------------------------------------------------------------------------------
   初期化関数
------------------------------------------------------------------------------*/
void InitGame(void)
{
	g_GameFramework.Initialize();

	g_SoundIndex = LoadSound("data/BGM/mega.wav");

	//	第一引数ー＞グローバル変数、第二引数ー＞0〜1までの数値
	//で音量が設定できます
	SetVolume(g_SoundIndex, 0.5f);

	PlaySound(g_SoundIndex, 256);
	
}

/*------------------------------------------------------------------------------
   終了処理をする関数
------------------------------------------------------------------------------*/
void UninitGame()
{
	g_GameFramework.Finalize();

}

/*------------------------------------------------------------------------------
   更新処理をする関数
------------------------------------------------------------------------------*/
void UpdateGame(void)
{
	g_GameFramework.Update();


	//スペースキーが押されていて、フェード処理中ではないとき
	if (GetKeyboardTrigger(DIK_RETURN) && GetFadeState() == FADE_NONE) {

		//RESULTへ移行する
		SceneTransition(SCENE_RESULT);
	}
}

/*------------------------------------------------------------------------------
   描画処理をする関数
------------------------------------------------------------------------------*/
void DrawGame(void)
{
	g_GameFramework.Draw();

}

